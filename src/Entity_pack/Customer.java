package Entity_pack;

public class Customer {
    private int id;
    private String name;

    public Customer(int id, String name, String phone) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }
    public String getName() {
        return name;
    }

    public void setId(int id) {
        this.id = id;
    }
    public void setName(String name) {
        this.name = name;
    }
}
